/**
 * Sintaxis bucle for
 * for(int contador=0;contador<=10;contador++){
 *
 * }
 */

public class BucleFor {
    public static void main(String[] args) {
        /**
         * Mostrar 10veces la palabra ECO
         */
        for(int i=1;i<=10;i++) {
            /*pueden ser muchs lineas de código*/
            System.out.println("ECO "+i);

        }


       /* ** Otro ejemplo**/
       /* **Mostrar todos los años de forma decreciente desde 2025 hasta el 2000 ** */
        for(int i=2025;i>=2000;i--) {
            System.out.println("Año "+i);
        }



    }
}
